class Integer {
    constructor(value) {
        this.value = value
    }
}

class Decimal {
    constructor(value) {
        this.value = value
    }
}

class Fraction {
    constructor(numerator, denominator) {
        this.numerator = numerator
        this.denominator = denominator
    }
}

class Operation {
    constructor(operation) {
        this.operation = operation
    }
}